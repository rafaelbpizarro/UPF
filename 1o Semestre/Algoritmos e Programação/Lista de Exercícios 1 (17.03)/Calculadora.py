print("CALCULADORA")
v1 = float(input("Qual o primeiro valor? "))
v2 = float(input("Qual o segundo valor? "))
op = str(input("Qual a operação a ser realizada? (+, -, * ou /) "))
if op == "+":
    print(v1 + v2)
if op == "-":
    print (v1 - v2)
if op == "*":
    print (v1 * v2)
if op == "/":
    print (v1 / v2)
